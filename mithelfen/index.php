<p id="Meshkit">&nbsp;</p>
<hr>
<h4>Meshkit</h4>
<p>Das Meshkit gibt uns die Möglichkeit, individuelle und fertig konfigurierte Firmwareimages für Router auszuliefern. Mit dem Meshkit konnten wir die Komplexität und Anzahl der Schritte vom gekaufen Router zum einsatzbereiten Weimarnetzgerät extrem verringern und für Nutzer einen inzwischen recht einfachen Weg zur Teilnahme am Netz bereiten.</p>
<p>Unsere angepasste Version des Meshkit kann man unter <a href="https://github.com/weimarnetz/meshkit">https://github.com/weimarnetz/meshkit</a> finden.</p>
<p>Python ist für dich nicht nur eine Schlange? Dann passt du bestens zum Meshkit!</p>

