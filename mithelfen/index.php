<h3>Wie kann ich bei der Weiterentwicklung des Weimarnetz mithelfen?</h3>
<p>Es gibt im Weimarnetz viele Möglichkeiten &mdash; neben dem Aufstellen eines Routers &mdash; sich einzubringen und an spanndenden Projekten mitzuarbeiten und so das Weimarnetz zu verbessern und lebendig zu gestalten. Einige mögliche Einstiegspunkte wollen wir im folgenden vorstellen.</p>
<p>Grundsätzlich gilt: Wir arbeiten im Team an Projekten und Problemen, und helfen uns gegenseitig. Aufgaben werden auf mehrere Schultern verteilt um zu vermeiden, dass aufgebautes Wissen verloren geht, falls jemand Weimar verlässt oder aus anderen Gründen nicht mehr mitarbeiten kann. Durch regelmäßige Treffen halten wir uns gegenseitig auf dem Laufenden. Auch hier soll sich die in Freifunk-Communities immer wieder propagierte Dezentralität widerspiegeln.</p>
<p>Die Ergebnisse unserer Arbeit stellen wir an öffentlich zugänglichen Orten (z.B. <a href="https://github.com/weimarnetz/">Github</a> oder als Download auf dieser Website) allen zur Verfügung und statten sie mit einer OpenSource-Lizenz aus. Regelmäßig tauschen wir uns mit anderen Freifunk-Communities aus, um gemeinsam Ideen zu erarbeiten, Resourcen effizient zu nutzen und sich unter Umständen doppelte Arbeit zu ersparen.</p>
<p>Zur gemeinsamen Koordination der anfallenden Aufgaben verwenden wir <a href="https://www.trello.com/weimarnetz">Trello</a> und sehen so transparent, was gerade ansteht und wo Hilfe benötigt wird.</p>


